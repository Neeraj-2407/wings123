x = ["neeraj","sai","rohit"]
print(x[1])

x = ["neeraj","sai","rohit"] #negative indexing
print(x[-1])

x = ["neeraj","sai","rohit","prem","raghav","vamshi"] #range of index
print(x[1:5])

x = ["neeraj","sai","rohit","prem","raghav","vamshi"] #range of index beginning
print(x[:3])

x = ["neeraj","sai","rohit","prem","raghav","vamshi"] #range of index end
print(x[3:])

x = ["neeraj","sai","rohit","prem","raghav","vamshi"] #range of index(negative)
print(x[-3:-1])
